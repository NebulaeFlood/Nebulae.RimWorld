namespace Nebulae.RimWorld.UI.Controls
{
    /// <summary>
    /// 滚动条的可见性
    /// </summary>
    public enum ScrollBarVisibility
    {
        /// <summary>
        /// 当可见区域无法显示所有内容时显示
        /// </summary>
        Auto,

        /// <summary>
        /// 始终隐藏
        /// </summary>
        Hidden,

        /// <summary>
        /// 始终显示
        /// </summary>
        Visible
    }
}
